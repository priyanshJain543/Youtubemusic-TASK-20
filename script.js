const allSongs = document.querySelector("#all-songs");
const cardFront = document.querySelector(".card-front");
const leftCard = document.querySelector(".card-inner");
const lyricsText = document.querySelector("#lyrics-text");
const lyricsTitle = document.querySelector("#lyrics-title");
const play = document.querySelector("#play");
const backward = document.querySelector("#backward");
const forward = document.querySelector("#forward");
const seekbar = document.querySelector("#seekbar");
const searchInput = document.querySelector("#search-input");
const visualizer = document.querySelector("#visualizer");
const currentTimeDisplay = document.querySelector("#current-time");
const totalDurationDisplay = document.querySelector("#total-duration");
const volumeSlider = document.querySelector("#volume-slider");


let audio = new Audio();
let selectedSong = 0;
let isPlaying = false;

function formatTime(seconds) {
    let min = Math.floor(seconds / 60);
    let sec = Math.floor(seconds % 60);
    return `${min}:${sec < 10 ? '0' + sec : sec}`;
}

function renderPlayerUI() {
    let clutter = "";
    
    arr.forEach((elem, index) => {
        let isActive = (index == selectedSong) ? "active-song" : "";
        clutter += `
            <div class="song-card ${isActive}" id="${index}">
                <div class="part1" style="display:flex; align-items:center;">
                    <img src="${elem.img}" style="height:48px; width:48px; border-radius:6px; object-fit:cover;">
                    <h2 style="font-size:17px; margin-left:15px; font-weight:600;">${elem.songName}</h2>
                </div>
                <h6 style="font-weight:400; opacity:0.7;">${elem.time}</h6> 
            </div>`;
    });
    
    if (allSongs) allSongs.innerHTML = clutter;
    
    const current = arr[selectedSong];
    if (cardFront) cardFront.style.backgroundImage = `url('${current.img}')`;
    if (lyricsText) lyricsText.innerText = current.lyrics;
    if (lyricsTitle) lyricsTitle.innerText = current.songName;
    
    audio.src = current.url;


    if (leftCard) leftCard.classList.remove("is-flipped");

    if (typeof gsap !== "undefined") {
        gsap.from(".song-card", {
            x: 40,
            opacity: 0,
            duration: 0.4,
            stagger: 0.08,
            ease: "power2.out"
        });
    } else {
        console.warn("GSAP not loaded. Skipping animations.");
    }
}

function handlePlay() {
    audio.play().catch(err => console.error("Playback failed:", err));
    play.innerHTML = `<i class="ri-pause-mini-fill"></i>`;
    isPlaying = true;
    if (visualizer) visualizer.classList.add("animating");
}

function handlePause() {
    audio.pause();
    play.innerHTML = `<i class="ri-play-mini-fill"></i>`;
    isPlaying = false;
    if (visualizer) visualizer.classList.remove("animating");
}


renderPlayerUI();


play.addEventListener("click", () => {
    if (!isPlaying) handlePlay();
    else handlePause();
});

allSongs.addEventListener("click", (e) => {
    const card = e.target.closest('.song-card');
    if (card) {
        selectedSong = Number(card.id);
        renderPlayerUI();
        setTimeout(handlePlay, 100);
    }
});

forward.addEventListener("click", () => {
    if (selectedSong < arr.length - 1) {
        selectedSong++;
        renderPlayerUI();
        setTimeout(handlePlay, 100);
    }
});

backward.addEventListener("click", () => {
    if (selectedSong > 0) {
        selectedSong--;
        renderPlayerUI();
        setTimeout(handlePlay, 100);
    }
});

audio.addEventListener("timeupdate", () => {
    if (!isNaN(audio.duration)) {
        seekbar.value = (audio.currentTime / audio.duration) * 100;
        if (currentTimeDisplay) currentTimeDisplay.innerText = formatTime(audio.currentTime);
        if (totalDurationDisplay) totalDurationDisplay.innerText = formatTime(audio.duration);
    }
});

seekbar.addEventListener("input", () => {
    audio.currentTime = (seekbar.value * audio.duration) / 100;
});


if (volumeSlider) {
    volumeSlider.addEventListener("input", () => {
        audio.volume = volumeSlider.value;
    });
}


audio.addEventListener("ended", () => {
    forward.click();
});


if (searchInput) {
    searchInput.addEventListener("input", () => {
        const query = searchInput.value.toLowerCase();
        document.querySelectorAll(".song-card").forEach(card => {
            const title = card.querySelector("h2").innerText.toLowerCase();
            card.style.display = title.includes(query) ? "flex" : "none";
        });
    });
}

if (leftCard) {
    leftCard.addEventListener("click", () => {
        leftCard.classList.toggle("is-flipped"); 
    });
}